// ─── Utility Functions ───────────────────────────────────────────────────────

/**
 * Find a color by name (case-insensitive partial match).
 * @param {string} name
 * @returns {object|undefined}
 */
export function findByName(name) {
  const lower = name.toLowerCase();
  return allColors.find((c) => c.name.toLowerCase().includes(lower));
}

/**
 * Find all colors by name (case-insensitive partial match).
 * @param {string} name
 * @returns {object[]}
 */
export function searchByName(name) {
  const lower = name.toLowerCase();
  return allColors.filter((c) => c.name.toLowerCase().includes(lower));
}

/**
 * Find colors by hex code (exact match).
 * @param {string} hex
 * @returns {object|undefined}
 */
export function findByHex(hex) {
  const normalized = hex.startsWith("#") ? hex.toUpperCase() : `#${hex}`.toUpperCase();
  return allColors.find((c) => c.hex.toUpperCase() === normalized);
}

/**
 * Get all colors in a specific category.
 * @param {string} category - e.g. 'blues', 'reds', 'specialty'
 * @returns {object[]}
 */
export function getCategory(category) {
  return categories[category] ?? [];
}

/**
 * Get a list of all category names.
 * @returns {string[]}
 */
export function getCategoryNames() {
  return Object.keys(categories);
}

/**
 * Get a random car color.
 * @returns {object}
 */
export function randomColor() {
  return allColors[Math.floor(Math.random() * allColors.length)];
}

